/**
 * bench_raba.js — RABA Real-Time Adaptive Batch Authentication
 *
 * Replaces the analytical projection in the paper with a direct measurement
 * of RABA deadline compliance under realistic V2V traffic density scenarios.
 *
 * WHAT THIS MEASURES
 * ------------------
 * For each traffic density scenario (free flow, moderate, dense, dense+adversarial):
 *
 *   N_WINDOWS "BSM windows" (each representing one 100 ms broadcast period) are
 *   replayed. In each window, k proof arrivals are assigned to three priority
 *   classes with simulated arrival times drawn uniformly over [0, T_WINDOW=100 ms]:
 *
 *     Emergency (D_E = 200 ms, 5% of k) — individually verified, immediate
 *     Warning   (D_W = 500 ms, 25% of k) — batched up to k_W = floor(D_W/T1) = 13
 *     Routine   (D_R = 2000 ms, 70% of k) — batched up to k_R = floor(D_R/T1) = 52
 *
 *   RABA processing order: Emergency → Warning → Routine (EDF within each class).
 *   All verification calls use real Groth16 operations (snarkjs + groth16_batch_verify.js).
 *
 *   Per proof: deadline = sim_arrival_ms + D_class
 *              completion = wall-clock ms from window start
 *              met = completion ≤ deadline
 *
 * ADVERSARIAL SCENARIO
 * --------------------
 * In the dense+adversarial scenario, one proof per Routine batch is corrupted
 * before calling batchVerify. When batchVerify fails, the DCV (divide-and-conquer)
 * fallback algorithm is invoked. Total Routine wall-clock time includes DCV overhead.
 *
 * TRAFFIC DENSITY SCENARIOS (3-lane highway, R=300m, FHWA statistics)
 * --------------------------------------------------------------------
 *   Free flow  : ρ=10 veh/km/lane, k=18 peers
 *   Moderate   : ρ=17 veh/km/lane, k=30 peers
 *   Dense      : ρ=28 veh/km/lane, k=50 peers
 *   Dense+Adv  : ρ=28, k=50, w=1 corrupted proof per Routine batch
 *
 * PROOF POOL
 * ----------
 * Loads proofs from results/dcv_proofs.json (generated by bench_dcv.js).
 * If not available, generates POOL_MIN_SIZE fresh proofs using snarkjs.
 * Proofs are reused cyclically across windows — timing is not affected.
 *
 * Run:  node benchmark/bench_raba.js
 * Prerequisites: npm run setup && npm run gen-input
 *   (Pre-run:   node benchmark/bench_dcv.js  to warm the proof cache)
 */

"use strict";

const snarkjs = require("snarkjs");
const { batchVerify, buildBatchCurve } = require("./groth16_batch_verify");
const crypto  = require("crypto");
const fs      = require("fs");
const path    = require("path");
const os      = require("os");

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------
const WASM        = path.join("build", "ulp_v2v_auth_js", "ulp_v2v_auth.wasm");
const ZKEY        = path.join("keys",  "ulp_v2v_auth_final.zkey");
const VK          = path.join("keys",  "verification_key.json");
const IN          = path.join("build", "input.json");
const PROOF_CACHE = path.join("results", "dcv_proofs.json");

// ---------------------------------------------------------------------------
// RABA parameters (§ System Design)
// T1 = amortised per-proof batch time at k=1, measured on RPi 4 (Table II)
// ---------------------------------------------------------------------------
const T_WINDOW_MS = 100;   // BSM window duration (10 Hz)
const T1          = 38;    // measured single-proof batch verify time (ms)
const D_E         = 200;   // Emergency class deadline (ms)
const D_W         = 500;   // Warning class deadline (ms)
const D_R         = 2000;  // Routine class deadline (ms)
const K_W         = Math.floor(D_W / T1);  // = 13
const K_R         = Math.min(50, Math.floor(D_R / T1));  // = 50

// Priority class distribution (fraction of k neighbours per class)
const P_E = 0.05;
const P_W = 0.25;
// P_R = 0.70 (remainder)

// Scenarios
const SCENARIOS = [
    { label: "Free flow  (k=18)", k: 18, rho: 10, adv: false },
    { label: "Moderate   (k=30)", k: 30, rho: 17, adv: false },
    { label: "Dense      (k=50)", k: 50, rho: 28, adv: false },
    { label: "Dense+Adv  (k=50, w=1)", k: 50, rho: 28, adv: true  },
];

const N_WINDOWS  = 500;  // windows per scenario — 500 gives stable mean/P99/min-margin
const POOL_MIN   = 60;   // pool size ≥ dense-scenario batch (k=50) with headroom

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------
function detectHardware() {
    if (process.platform === "linux" && fs.existsSync("/proc/cpuinfo")) {
        const info = fs.readFileSync("/proc/cpuinfo", "utf8");
        const m = info.match(/^Model\s*:\s*(.+)$/m);
        if (m) return m[1].trim();
        const h = info.match(/^Hardware\s*:\s*(.+)$/m);
        if (h) return `Linux/${h[1].trim()}`;
    }
    const cpu = os.cpus()[0]?.model ?? "Unknown";
    return `${process.platform === "darwin" ? "macOS" : os.platform()} — ${cpu}`;
}

const mean = arr => arr.reduce((a, b) => a + b, 0) / arr.length;
const std  = arr => {
    const m = mean(arr);
    return Math.sqrt(arr.reduce((s, x) => s + (x - m) ** 2, 0) / arr.length);
};
const pct  = (arr, p) => {
    const s = [...arr].sort((a, b) => a - b);
    return s[Math.floor(p / 100 * s.length)] ?? s[s.length - 1];
};

/** Deep-copy a proof and flip one bit in pi_a to make it invalid. */
function corruptProof(proof) {
    const c = JSON.parse(JSON.stringify(proof));
    c.pi_a[0] = (BigInt(c.pi_a[0]) + BigInt(1)).toString();
    return c;
}

/**
 * DCV (divide-and-conquer) fallback — Algorithm 4 from § System Design.
 * Called when the initial batchVerify of a batch fails.
 * Recursively isolates corrupted proofs using O(w log k) sub-batch verifications.
 */
async function dcv(proofs, pubs, vk, curve, counter) {
    const k = proofs.length;
    if (k === 1) {
        counter.calls++;
        const r = await batchVerify(proofs, pubs, vk, curve);
        return { valid: r.valid, badIdx: r.valid ? [] : [0] };
    }
    const mid   = Math.floor(k / 2);
    counter.calls += 2;
    const lRes  = await batchVerify(proofs.slice(0, mid), pubs.slice(0, mid), vk, curve);
    const rRes  = await batchVerify(proofs.slice(mid),    pubs.slice(mid),    vk, curve);
    let badIdx  = [];
    if (!lRes.valid) {
        const rec = await dcv(proofs.slice(0, mid), pubs.slice(0, mid), vk, curve, counter);
        badIdx.push(...rec.badIdx);
    }
    if (!rRes.valid) {
        const rec = await dcv(proofs.slice(mid), pubs.slice(mid), vk, curve, counter);
        badIdx.push(...rec.badIdx.map(i => i + mid));
    }
    return { valid: badIdx.length === 0, badIdx };
}

// ---------------------------------------------------------------------------
// Proof pool — load from dcv cache or generate fresh
// ---------------------------------------------------------------------------
async function loadPool(minSize, baseInput) {
    // Try loading from dcv_proofs.json (bench_dcv.js generates this)
    if (fs.existsSync(PROOF_CACHE)) {
        try {
            const cached = JSON.parse(fs.readFileSync(PROOF_CACHE));
            if (Array.isArray(cached.proofs) && cached.proofs.length >= minSize) {
                console.log(`  Loaded ${cached.proofs.length} proofs from cache (${PROOF_CACHE}).`);
                return cached.proofs;
            }
            console.log(`  Cache has ${cached.proofs?.length ?? 0} proofs; need ${minSize} — generating more.`);
        } catch { /* fall through */ }
    }

    console.log(`  Generating ${minSize} proofs for pool (no cache found)...`);
    console.log(`  TIP: run bench_dcv.js first to cache ${minSize} proofs faster.`);
    const pool = [];
    for (let i = 0; i < minSize; i++) {
        const pkOt = (BigInt(baseInput.pkOt) + BigInt(i + 1)).toString();
        const inp  = { ...baseInput, pkOt };
        process.stdout.write(`  Proof ${i + 1}/${minSize}  \r`);
        const { proof, publicSignals } = await snarkjs.groth16.fullProve(inp, WASM, ZKEY);
        pool.push({ proof, publicSignals });
    }
    console.log(`  Generated ${minSize} proofs.                      `);
    fs.mkdirSync("results", { recursive: true });
    fs.writeFileSync(PROOF_CACHE, JSON.stringify({ k: minSize, proofs: pool }, null, 2));
    console.log(`  Proof pool saved to ${PROOF_CACHE}`);
    return pool;
}

// ---------------------------------------------------------------------------
// Run one RABA window for a given scenario
// ---------------------------------------------------------------------------
async function runWindow(pool, vk, curve, k, adv) {
    // Assign class counts (round to nearest, at least 1 Emergency)
    const n_E = Math.max(1, Math.round(P_E * k));
    const n_W = Math.round(P_W * k);
    const n_R = k - n_E - n_W;

    // Draw simulated arrival offsets (ms within the 100 ms window), sort ascending
    const arrivals = Array.from({ length: k }, (_, i) => ({
        sim: Math.random() * T_WINDOW_MS,   // simulated arrival time within window
        pi:  i % pool.length,               // pool index (cycling)
    })).sort((a, b) => a.sim - b.sim);

    // Assign priority classes in arrival order (earliest → Emergency, then Warning, rest Routine)
    const Eq = arrivals.slice(0, n_E).map(a => ({ ...a, cls: "E" }));
    const Wq = arrivals.slice(n_E, n_E + n_W).map(a => ({ ...a, cls: "W" }));
    const Rq = arrivals.slice(n_E + n_W).map(a => ({ ...a, cls: "R" }));

    const results   = [];  // { cls, wallMs, deadline, met, margin }
    const wallStart = performance.now();

    // ------------------------------------------------------------------
    // Emergency: individually verify, in arrival order
    // ------------------------------------------------------------------
    for (const e of Eq) {
        const { proof, publicSignals } = pool[e.pi];
        await snarkjs.groth16.verify(vk, publicSignals, proof);
        const wallMs   = performance.now() - wallStart;
        const deadline = e.sim + D_E;
        const met      = wallMs <= deadline;
        results.push({ cls: "E", wallMs, deadline, met, margin: deadline - wallMs });
    }

    // ------------------------------------------------------------------
    // Warning: batch of up to K_W proofs per sub-batch (EDF — earliest arrival first)
    // ------------------------------------------------------------------
    for (let i = 0; i < Wq.length; i += K_W) {
        const sub    = Wq.slice(i, i + K_W);
        const proofs = sub.map(e => pool[e.pi].proof);
        const pubs   = sub.map(e => pool[e.pi].publicSignals);
        await batchVerify(proofs, pubs, vk, curve);
        const wallMs = performance.now() - wallStart;
        for (const e of sub) {
            const deadline = e.sim + D_W;
            const met      = wallMs <= deadline;
            results.push({ cls: "W", wallMs, deadline, met, margin: deadline - wallMs });
        }
    }

    // ------------------------------------------------------------------
    // Routine: batch of up to K_R proofs per sub-batch; DCV on failure
    // ------------------------------------------------------------------
    let dcvCalls = 0;
    for (let i = 0; i < Rq.length; i += K_R) {
        const sub     = Rq.slice(i, i + K_R);
        let proofs    = sub.map(e => pool[e.pi].proof);
        const pubs    = sub.map(e => pool[e.pi].publicSignals);

        if (adv && sub.length > 1) {
            // Inject one corrupted proof at a random position
            const badPos = Math.floor(Math.random() * sub.length);
            proofs       = proofs.map((p, j) => j === badPos ? corruptProof(p) : p);

            const init = await batchVerify(proofs, pubs, vk, curve);
            if (!init.valid) {
                const counter = { calls: 0 };
                await dcv(proofs, pubs, vk, curve, counter);
                dcvCalls += counter.calls;
            }
        } else {
            await batchVerify(proofs, pubs, vk, curve);
        }

        const wallMs = performance.now() - wallStart;
        for (const e of sub) {
            const deadline = e.sim + D_R;
            const met      = wallMs <= deadline;
            results.push({ cls: "R", wallMs, deadline, met, margin: deadline - wallMs });
        }
    }

    return { results, n_E, n_W, n_R, dcvCalls };
}

// ---------------------------------------------------------------------------
// Aggregate per-class statistics across all windows
// ---------------------------------------------------------------------------
function aggregateClass(cls, windowResults) {
    const rows  = windowResults.flatMap(w => w.results.filter(r => r.cls === cls));
    if (rows.length === 0) return null;
    const met   = rows.filter(r => r.met).length;
    const walls = rows.map(r => r.wallMs);
    const margins = rows.map(r => r.margin);
    return {
        n:          rows.length,
        hitRate:    (met / rows.length * 100).toFixed(1) + "%",
        hitN:       met,
        meanWall_ms: parseFloat(mean(walls).toFixed(1)),
        p99Wall_ms:  parseFloat(pct(walls, 99).toFixed(1)),
        meanMargin_ms: parseFloat(mean(margins).toFixed(1)),
        minMargin_ms:  parseFloat(Math.min(...margins).toFixed(1)),
    };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
async function main() {
    const hw = detectHardware();

    console.log("\n" + "=".repeat(72));
    console.log("  ULP-V2V-Auth — RABA Deadline Compliance Benchmark");
    console.log("  Direct measurement (replaces analytical projection in paper)");
    console.log(`  Hardware : ${hw}`);
    console.log(`  N_WINDOWS = ${N_WINDOWS} per scenario  |  k_W = ${K_W}  |  k_R = ${K_R}`);
    console.log("=".repeat(72));

    for (const f of [WASM, ZKEY, VK, IN]) {
        if (!fs.existsSync(f)) {
            console.error(`Missing: ${f} — run npm run setup && npm run gen-input`);
            process.exit(1);
        }
    }

    const baseInput  = JSON.parse(fs.readFileSync(IN));
    const vk         = JSON.parse(fs.readFileSync(VK));
    const batchCurve = await buildBatchCurve();

    // Load proof pool
    console.log(`\n[Pool] Loading proof pool (min ${POOL_MIN} proofs)...`);
    const pool = await loadPool(POOL_MIN, baseInput);

    const allScenarioResults = [];

    // ---------------------------------------------------------------------------
    // Run each scenario
    // ---------------------------------------------------------------------------
    for (const sc of SCENARIOS) {
        console.log(`\n${"─".repeat(72)}`);
        console.log(`[Scenario] ${sc.label}`);
        const n_E = Math.max(1, Math.round(P_E * sc.k));
        const n_W = Math.round(P_W * sc.k);
        const n_R = sc.k - n_E - n_W;
        console.log(`  Class distribution: E=${n_E}  W=${n_W}  R=${n_R}  (total k=${sc.k})`);
        console.log(`  k_W=${K_W} (Warning batch max)  k_R=${K_R} (Routine batch max)${sc.adv ? "  [+DCV w=1]" : ""}`);
        console.log(`${"─".repeat(72)}`);

        const windowResults = [];

        const PRINT_EVERY = Math.max(1, Math.floor(N_WINDOWS / 10)); // print ~10 updates
        for (let w = 0; w < N_WINDOWS; w++) {
            const res = await runWindow(pool, vk, batchCurve, sc.k, sc.adv);
            windowResults.push(res);

            // Progress: print every PRINT_EVERY windows and on the last window
            if ((w + 1) % PRINT_EVERY === 0 || w === N_WINDOWS - 1) {
                const doneSoFar = windowResults.slice(-PRINT_EVERY);
                const misses = doneSoFar.flatMap(r => r.results).filter(r => !r.met).length;
                const dcvAcc = doneSoFar.reduce((s, r) => s + r.dcvCalls, 0);
                const dcvNote = sc.adv ? `  DCV acc=${dcvAcc}` : "";
                process.stdout.write(
                    `  [${w + 1}/${N_WINDOWS}] misses_in_last_batch=${misses}${dcvNote}  \r`
                );
            }
        }
        console.log(""); // newline after progress

        // Aggregate
        const Eagg = aggregateClass("E", windowResults);
        const Wagg = aggregateClass("W", windowResults);
        const Ragg = aggregateClass("R", windowResults);
        const totalDcvCalls = windowResults.reduce((s, w) => s + w.dcvCalls, 0);

        console.log(`\n  ── Per-class summary ──`);
        for (const [cls, agg, D, Kc] of [["E", Eagg, D_E, 1], ["W", Wagg, D_W, K_W], ["R", Ragg, D_R, K_R]]) {
            if (!agg) { console.log(`  Class ${cls}: (no proofs in this class)`); continue; }
            const deadlineMark = parseFloat(agg.minMargin_ms) >= 0 ? "✓ ALL MET" : "✗ MISS";
            console.log(
                `  Class ${cls} (D_${cls}=${D} ms, k_max=${Kc}): ` +
                `hit=${agg.hitRate}  mean=${agg.meanWall_ms} ms  P99=${agg.p99Wall_ms} ms  ` +
                `min-margin=${agg.minMargin_ms} ms  → ${deadlineMark}`
            );
        }
        if (sc.adv) {
            console.log(`  DCV total calls: ${totalDcvCalls} over ${N_WINDOWS} windows (avg ${(totalDcvCalls / N_WINDOWS).toFixed(1)}/window)`);
        }

        allScenarioResults.push({
            scenario:    sc.label,
            k:           sc.k,
            rho:         sc.rho,
            adversarial: sc.adv,
            n_E, n_W, n_R,
            Emergency:   Eagg,
            Warning:     Wagg,
            Routine:     Ragg,
            totalDcvCalls,
            windows:     N_WINDOWS,
        });
    }

    // ---------------------------------------------------------------------------
    // Master summary table
    // ---------------------------------------------------------------------------
    console.log("\n" + "=".repeat(72));
    console.log("  RABA Deadline Compliance — Master Summary");
    console.log("=".repeat(72));
    console.log(
        `  ${"Scenario".padEnd(28)} ${"Class".padEnd(6)} ` +
        `${"Hit".padStart(6)} ${"MeanWall".padStart(10)} ${"P99Wall".padStart(9)} ` +
        `${"MinMargin".padStart(10)} ${"Deadline".padStart(10)} ${"Status".padStart(8)}`
    );
    console.log("  " + "─".repeat(90));

    for (const sr of allScenarioResults) {
        for (const [cls, agg, D] of [["E", sr.Emergency, D_E], ["W", sr.Warning, D_W], ["R", sr.Routine, D_R]]) {
            if (!agg) continue;
            const met = parseFloat(agg.minMargin_ms) >= 0;
            console.log(
                `  ${sr.scenario.padEnd(28)} ${cls.padEnd(6)} ` +
                `${agg.hitRate.padStart(6)} ` +
                `${(agg.meanWall_ms + " ms").padStart(10)} ` +
                `${(agg.p99Wall_ms + " ms").padStart(9)} ` +
                `${(agg.minMargin_ms + " ms").padStart(10)} ` +
                `${("≤" + D + " ms").padStart(10)} ` +
                `${(met ? "✓ MET" : "✗ MISS").padStart(8)}`
            );
        }
    }

    // ---------------------------------------------------------------------------
    // Save results
    // ---------------------------------------------------------------------------
    fs.mkdirSync("results", { recursive: true });
    const outPath = path.join("results", "bench_raba.json");
    fs.writeFileSync(outPath, JSON.stringify({
        benchmark:   "raba_deadline_compliance",
        description: "Direct measurement of RABA EDF scheduling with real Groth16 batch verification",
        hardware:    hw,
        circuit:     "ULP_V2V_Auth(depth=8, constraints=5069, BN254)",
        timestamp:   new Date().toISOString(),
        params: {
            T_window_ms: T_WINDOW_MS,
            T1_ms:       T1,
            D_E_ms:      D_E,
            D_W_ms:      D_W,
            D_R_ms:      D_R,
            k_W:         K_W,
            k_R:         K_R,
            P_E:         P_E,
            P_W:         P_W,
            N_windows:   N_WINDOWS,
            pool_size:   pool.length,
        },
        scenarios: allScenarioResults,
    }, null, 2));

    console.log(`\nResults saved → ${outPath}`);
    await batchCurve.terminate();
}

main().catch(err => { console.error(err); process.exit(1); });
