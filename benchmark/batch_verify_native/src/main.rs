//! bench_batch_native — Native Groth16 Batch Verification Benchmark
//!
//! Measures sequential vs batch Groth16 verification on BN254 using arkworks,
//! removing JavaScript/BigInt per-call overhead to isolate the true algebraic speedup.
//!
//! Algorithm (Bellare et al. 1998, applied to Groth16):
//!   Individual (per proof j): e(A_j, B_j) · e(-L_j, γ) · e(-C_j, δ) == e(α,β)
//!     Cost: 3 Miller loops + 1 final exp, k times → 3k ML + k FE
//!
//!   Batch (k proofs together): sample random ρ_j, then check:
//!     ∏_j e(ρ_j·A_j, B_j) · e(-∑ρ_j·α, β) · e(-∑ρ_j·L_j, γ) · e(-∑ρ_j·C_j, δ) = 1_GT
//!     Cost: k+3 Miller loops + 1 final exp (plus MSM for G1 accumulation)
//!
//! Reads:
//!   ../../keys/verification_key.json   (snarkjs format, run from repo root)
//!   ../../results/dcv_proofs.json      (proof pool, generated by bench_dcv.js)
//!
//! Outputs:
//!   ../../results/bench_batch_native.json

use ark_bn254::{Bn254, Fr, Fq, Fq2, G1Affine, G1Projective, G2Affine, G2Prepared};
use ark_ec::{AffineRepr, CurveGroup, pairing::Pairing};
use ark_ff::{PrimeField, Zero};
use rand::{Rng, thread_rng};
use serde_json::Value;
use std::{fs, time::Instant, str::FromStr};
use std::ops::Neg;

// ---------------------------------------------------------------------------
// Parsing helpers: snarkjs JSON → arkworks types
//
// snarkjs field elements are decimal strings of the canonical (non-Montgomery)
// representation. ark-ff's FromStr parses these directly.
//
// G1 affine: [x_dec, y_dec, "1"]
// G2 affine: [[x_c0, x_c1], [y_c0, y_c1], ["1", "0"]]
//   where Fq2 = Fq[u]/(u²+1), element a + b·u stored as [a, b] (c0, c1)
// ---------------------------------------------------------------------------

fn parse_fq(s: &str) -> Fq {
    Fq::from_str(s).expect("invalid Fq decimal string")
}

fn parse_fr(s: &str) -> Fr {
    Fr::from_str(s).expect("invalid Fr decimal string")
}

fn parse_g1(v: &Value) -> G1Affine {
    let x = parse_fq(v[0].as_str().unwrap());
    let y = parse_fq(v[1].as_str().unwrap());
    // z = "1" (affine); use unchecked since proofs come from trusted snarkjs pool
    G1Affine::new_unchecked(x, y)
}

fn parse_g2(v: &Value) -> G2Affine {
    let x = Fq2::new(
        parse_fq(v[0][0].as_str().unwrap()), // c0 (real part)
        parse_fq(v[0][1].as_str().unwrap()), // c1 (imaginary part)
    );
    let y = Fq2::new(
        parse_fq(v[1][0].as_str().unwrap()),
        parse_fq(v[1][1].as_str().unwrap()),
    );
    G2Affine::new_unchecked(x, y)
}

// ---------------------------------------------------------------------------
// Verification key (parsed from snarkjs JSON)
// ---------------------------------------------------------------------------

struct Vk {
    alpha_g1:  G1Affine,
    beta_g2:   G2Affine,
    gamma_g2:  G2Affine,
    delta_g2:  G2Affine,
    ic:        Vec<G1Affine>,
    // e(α, β) precomputed once for individual-verify comparisons
    alpha_beta: ark_ec::pairing::PairingOutput<Bn254>,
}

impl Vk {
    fn from_json(v: &Value) -> Self {
        let alpha_g1 = parse_g1(&v["vk_alpha_1"]);
        let beta_g2  = parse_g2(&v["vk_beta_2"]);
        let gamma_g2 = parse_g2(&v["vk_gamma_2"]);
        let delta_g2 = parse_g2(&v["vk_delta_2"]);
        let ic: Vec<G1Affine> = v["IC"].as_array().unwrap()
            .iter().map(|p| parse_g1(p)).collect();
        let alpha_beta = Bn254::pairing(alpha_g1, beta_g2);
        Vk { alpha_g1, beta_g2, gamma_g2, delta_g2, ic, alpha_beta }
    }
}

// ---------------------------------------------------------------------------
// Proof
// ---------------------------------------------------------------------------

#[derive(Clone)]
struct Proof {
    a: G1Affine,
    b: G2Affine,
    c: G1Affine,
}

impl Proof {
    fn from_json(v: &Value) -> Self {
        Proof {
            a: parse_g1(&v["pi_a"]),
            b: parse_g2(&v["pi_b"]),
            c: parse_g1(&v["pi_c"]),
        }
    }
}

// ---------------------------------------------------------------------------
// L = IC[0] + ∑_i pub_i · IC[i+1]   (Groth16 public-input commitment)
// ---------------------------------------------------------------------------

fn compute_l(pub_inputs: &[Fr], ic: &[G1Affine]) -> G1Affine {
    let mut l = G1Projective::from(ic[0]);
    for (i, s) in pub_inputs.iter().enumerate() {
        l += G1Projective::from(ic[i + 1]) * s;
    }
    G1Affine::from(l)
}

// ---------------------------------------------------------------------------
// Individual verify
//   e(A, B) · e(-L, γ) · e(-C, δ) == e(α, β)
//   Cost: 3 Miller loops + 1 final exp  (e(α,β) is precomputed)
// ---------------------------------------------------------------------------

fn verify_one(vk: &Vk, proof: &Proof, pub_inputs: &[Fr]) -> bool {
    let l     = compute_l(pub_inputs, &vk.ic);
    let neg_l = l.into_group().neg().into_affine();
    let neg_c = proof.c.into_group().neg().into_affine();

    let ml = Bn254::multi_miller_loop(
        [proof.a, neg_l, neg_c],
        [
            G2Prepared::from(proof.b),
            G2Prepared::from(vk.gamma_g2),
            G2Prepared::from(vk.delta_g2),
        ],
    );
    Bn254::final_exponentiation(ml)
        .expect("final exp failed")
        == vk.alpha_beta
}

// ---------------------------------------------------------------------------
// Batch verify (Bellare et al., k+3 pairings)
//   ∏_j e(ρ_j·A_j, B_j) · e(-(∑ρ_j)·α, β) · e(-∑ρ_j·L_j, γ) · e(-∑ρ_j·C_j, δ) = 1_GT
//   Cost: k+3 Miller loops + 1 final exp  (+ MSM for G1 accumulation)
//
//   Returns (valid, pairing_count)
// ---------------------------------------------------------------------------

fn verify_batch(vk: &Vk, proofs: &[Proof], pub_inputs_vec: &[Vec<Fr>]) -> (bool, usize) {
    assert_eq!(proofs.len(), pub_inputs_vec.len());
    let k = proofs.len();

    // Sample random scalars ρ_j ∈ Fr* using OS randomness
    let mut rng = thread_rng();
    let rhos: Vec<Fr> = (0..k)
        .map(|_| {
            let bytes: [u8; 32] = rng.gen();
            Fr::from_le_bytes_mod_order(&bytes)
        })
        .collect();

    // Accumulate aggregated G1 points
    let mut sum_rho = Fr::zero();
    let mut agg_rl  = G1Projective::zero(); // ∑ ρ_j · L_j
    let mut agg_rc  = G1Projective::zero(); // ∑ ρ_j · C_j

    // ρ_j · A_j for each j (kept separate — B_j vary so we can't fold A side further)
    let scaled_a: Vec<G1Affine> = proofs.iter().zip(rhos.iter()).map(|(p, rho)| {
        G1Affine::from(G1Projective::from(p.a) * rho)
    }).collect();

    for ((proof, pub_inp), rho) in proofs.iter().zip(pub_inputs_vec.iter()).zip(rhos.iter()) {
        sum_rho += rho;
        let l  = compute_l(pub_inp, &vk.ic);
        agg_rl += G1Projective::from(l) * rho;
        agg_rc += G1Projective::from(proof.c) * rho;
    }

    // Negate the three aggregated points
    let neg_sum_rho_alpha = G1Affine::from(G1Projective::from(vk.alpha_g1) * sum_rho)
        .into_group().neg().into_affine();
    let neg_agg_rl = G1Affine::from(agg_rl).into_group().neg().into_affine();
    let neg_agg_rc = G1Affine::from(agg_rc).into_group().neg().into_affine();

    // Build k+3 pairs for the multi-Miller-loop
    let g1_iter = scaled_a.iter().copied()
        .chain([neg_sum_rho_alpha, neg_agg_rl, neg_agg_rc]);

    let g2_iter = proofs.iter().map(|p| G2Prepared::from(p.b))
        .chain([
            G2Prepared::from(vk.beta_g2),
            G2Prepared::from(vk.gamma_g2),
            G2Prepared::from(vk.delta_g2),
        ]);

    let ml = Bn254::multi_miller_loop(g1_iter, g2_iter);
    let gt = Bn254::final_exponentiation(ml).expect("final exp failed");

    // PairingOutput::zero() is the multiplicative identity (Fq12::one()) in arkworks GT
    (gt.is_zero(), k + 3)
}

// ---------------------------------------------------------------------------
// Timing helpers
// ---------------------------------------------------------------------------

fn mean(v: &[f64]) -> f64 { v.iter().sum::<f64>() / v.len() as f64 }

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

fn main() {
    // Detect running context: we expect to be called from repo root
    // (keys/ and results/ relative to cwd, same as all other bench scripts).
    let vk_path    = "keys/verification_key.json";
    let pool_path  = "results/dcv_proofs.json";
    let out_path   = "results/bench_batch_native.json";

    for path in [vk_path, pool_path] {
        if !std::path::Path::new(path).exists() {
            eprintln!("Missing: {path}");
            eprintln!("Run this binary from the repository root after running:");
            eprintln!("  node benchmark/bench_dcv.js   (to generate the proof pool)");
            std::process::exit(1);
        }
    }

    // Load verification key
    let vk_json: Value = serde_json::from_str(
        &fs::read_to_string(vk_path).expect("cannot read vk")
    ).expect("cannot parse vk JSON");
    let vk = Vk::from_json(&vk_json);
    println!("Loaded verification key (nPublic={})", vk.ic.len() - 1);

    // Load proof pool
    let pool_json: Value = serde_json::from_str(
        &fs::read_to_string(pool_path).expect("cannot read proof pool")
    ).expect("cannot parse pool JSON");

    let pool_arr = pool_json["proofs"].as_array().expect("proofs array missing");
    let proofs: Vec<Proof> = pool_arr.iter()
        .map(|e| Proof::from_json(&e["proof"]))
        .collect();
    let pub_inputs: Vec<Vec<Fr>> = pool_arr.iter()
        .map(|e| {
            e["publicSignals"].as_array().unwrap()
                .iter()
                .map(|s| parse_fr(s.as_str().unwrap()))
                .collect()
        })
        .collect();

    let pool_size = proofs.len();
    println!("Loaded {} proofs from pool", pool_size);

    // Verify one proof so we know the key+proof are compatible
    {
        let ok = verify_one(&vk, &proofs[0], &pub_inputs[0]);
        if !ok {
            eprintln!("ERROR: proof[0] failed individual verification — key/proof mismatch!");
            std::process::exit(1);
        }
        let (ok_b, _) = verify_batch(&vk, &proofs[..1], &pub_inputs[..1]);
        if !ok_b {
            eprintln!("ERROR: proof[0] failed batch verification — implementation bug!");
            std::process::exit(1);
        }
    }
    println!("Sanity check passed (both individual and batch verify proof[0]).\n");

    // Warm-up pass (1 iteration each at k=1 to warm instruction cache)
    {
        let _ = verify_one(&vk, &proofs[0], &pub_inputs[0]);
        let _ = verify_batch(&vk, &proofs[..1], &pub_inputs[..1]);
    }

    const N_REPEAT: usize = 3;
    let batch_sizes: &[usize] = &[1, 5, 10, 20, 30, 50];

    println!("{:=<72}", "");
    println!("  Native Groth16 Batch Verification Benchmark (arkworks, BN254)");
    println!("  Sequential: 3k Miller loops + k final-exp");
    println!("  Batch     : k+3 Miller loops + 1 final-exp  (Bellare et al.)");
    println!("{:=<72}", "");
    println!("  {:>4}  {:>10}  {:>10}  {:>8}  {:>8}",
             "k", "Seq (ms)", "Batch (ms)", "Actual×", "Theory×");
    println!("  {:-<54}", "");

    let mut json_results = Vec::new();

    for &k in batch_sizes {
        if k > pool_size {
            println!("  k={k}: skipped (pool has only {pool_size} proofs)");
            continue;
        }

        let k_proofs = &proofs[..k];
        let k_pubs   = &pub_inputs[..k];

        // --- Sequential: k individual verifies ---
        let mut seq_times = Vec::with_capacity(N_REPEAT);
        for _ in 0..N_REPEAT {
            let t = Instant::now();
            for (p, inp) in k_proofs.iter().zip(k_pubs.iter()) {
                let _ = verify_one(&vk, p, inp);
            }
            seq_times.push(t.elapsed().as_secs_f64() * 1e3);
        }
        let seq_ms = mean(&seq_times);

        // --- Batch: all k proofs in one batch call ---
        let mut batch_times = Vec::with_capacity(N_REPEAT);
        let mut last_pairing_count = 0;
        for _ in 0..N_REPEAT {
            let t = Instant::now();
            let (_, pc) = verify_batch(&vk, k_proofs, k_pubs);
            batch_times.push(t.elapsed().as_secs_f64() * 1e3);
            last_pairing_count = pc;
        }
        let batch_ms = mean(&batch_times);

        let actual_speedup     = seq_ms / batch_ms;
        let theoretical_speedup = (3 * k) as f64 / (k + 3) as f64;

        println!("  {:>4}  {:>10.1}  {:>10.1}  {:>8.2}  {:>8.2}",
                 k, seq_ms, batch_ms, actual_speedup, theoretical_speedup);

        json_results.push(serde_json::json!({
            "k": k,
            "seq_ms": seq_ms,
            "batch_ms": batch_ms,
            "actual_speedup": actual_speedup,
            "theoretical_speedup": theoretical_speedup,
            "pairings_individual": 3 * k,
            "pairings_batch": last_pairing_count,
            "n_repeat": N_REPEAT,
        }));
    }

    println!("{:=<72}", "");

    // Hardware info
    let hw = {
        if std::path::Path::new("/proc/cpuinfo").exists() {
            let info = fs::read_to_string("/proc/cpuinfo").unwrap_or_default();
            if let Some(m) = info.lines()
                .find(|l| l.starts_with("Model"))
                .and_then(|l| l.split(':').nth(1))
            {
                m.trim().to_string()
            } else {
                "Linux/ARM".to_string()
            }
        } else {
            format!("{}/{}", std::env::consts::OS, std::env::consts::ARCH)
        }
    };
    println!("\nHardware: {hw}");

    // Save JSON
    fs::create_dir_all("results").expect("cannot create results/");
    let output = serde_json::json!({
        "benchmark":   "native_groth16_batch_verify",
        "description": "arkworks BN254 sequential vs batch, no JS/BigInt overhead",
        "library":     "ark-bn254 0.4 (arkworks-rs)",
        "circuit":     "ULP_V2V_Auth(depth=8, constraints=5069, BN254)",
        "hardware":    hw,
        "n_repeat":    N_REPEAT,
        "batch_sizes": batch_sizes,
        "results":     json_results,
    });
    fs::write(out_path, serde_json::to_string_pretty(&output).unwrap())
        .expect("cannot write output");
    println!("Results saved → {out_path}");
}
